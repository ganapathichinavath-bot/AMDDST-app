// ── SEARCH FUNCTIONS ──────────────────────────────────────────────────────────
export function searchHotels(slots) {
  let results = [...hotelDB];
  if (slots['hotel-area']) results = results.filter(h => h.area === slots['hotel-area']);
  if (slots['hotel-pricerange']) results = results.filter(h => h.pricerange === slots['hotel-pricerange']);
  if (slots['hotel-type']) results = results.filter(h => h.type === slots['hotel-type']);
  if (slots['hotel-parking'] === 'yes') results = results.filter(h => h.parking === 'yes');
  if (slots['hotel-internet'] === 'yes') results = results.filter(h => h.internet === 'yes');
  if (slots['hotel-stars']) results = results.filter(h => h.stars === parseInt(slots['hotel-stars']));
  // If no results with strict filters, relax and return top 3
  if (results.length === 0) results = [...hotelDB];
  return results.slice(0, 3);
}

