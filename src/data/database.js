export function parseSlots(beliefStr) {
  const slots = {};
  if (!beliefStr || beliefStr === 'none') return slots;
  beliefStr.split('|').forEach(pair => {
    const [key, value] = pair.split('=').map(s => s.trim());
    if (key && value) slots[key] = value;
  });
  return slots;
}

// ── GREETING DETECTION ────────────────────────────────────────────────────────
export function isGreeting(text) {
  const greetings = ['hi', 'hello', 'hey', 'hiya', 'howdy', 'greetings', 'good morning', 'good afternoon', 'good evening', 'sup', 'what\'s up', 'whats up', 'yo'];
  const t = text.toLowerCase().trim();
  return greetings.some(g => t === g || t.startsWith(g + ' ') || t.startsWith(g + '!') || t.startsWith(g + ','));
}

